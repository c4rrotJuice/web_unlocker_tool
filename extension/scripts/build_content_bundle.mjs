#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const extensionRoot = path.resolve(__dirname, "..");
const entryFile = path.resolve(extensionRoot, "content/index.js");
const outputFile = path.resolve(extensionRoot, "content/unlock_content.bundle.js");

const IMPORT_NAMED_RE = /^import\s*\{\s*([^}]+)\s*\}\s*from\s*["'](.+?)["'];?\s*$/gm;
const IMPORT_SIDE_EFFECT_RE = /^import\s*["'](.+?)["'];?\s*$/gm;
const EXPORT_FUNCTION_RE = /^export\s+function\s+([A-Za-z0-9_]+)\s*\(/gm;
const EXPORT_CONST_RE = /^export\s+const\s+([A-Za-z0-9_]+)\s*=/gm;
const EXPORT_LET_RE = /^export\s+let\s+([A-Za-z0-9_]+)\s*=/gm;
const EXPORT_VAR_RE = /^export\s+var\s+([A-Za-z0-9_]+)\s*=/gm;

function toPosix(value) {
  return value.split(path.sep).join(path.posix.sep);
}

function moduleId(filePath) {
  return toPosix(path.relative(extensionRoot, filePath));
}

function resolveImport(fromFile, specifier) {
  if (!specifier.startsWith(".")) {
    throw new Error(`Unsupported non-relative import '${specifier}' in ${fromFile}`);
  }
  const base = path.resolve(path.dirname(fromFile), specifier);
  if (path.extname(base)) {
    return base;
  }
  const withJs = `${base}.js`;
  if (fs.existsSync(withJs)) return withJs;
  const withIndexJs = path.join(base, "index.js");
  if (fs.existsSync(withIndexJs)) return withIndexJs;
  throw new Error(`Unable to resolve import '${specifier}' from ${fromFile}`);
}

function parseModule(filePath) {
  const source = fs.readFileSync(filePath, "utf8");
  const dependencies = [];
  let transformed = source;
  const exports = [];

  transformed = transformed.replace(IMPORT_NAMED_RE, (_, names, specifier) => {
    const depPath = resolveImport(filePath, specifier);
    dependencies.push(depPath);
    const importNames = names
      .split(",")
      .map((item) => item.trim())
      .filter(Boolean)
      .join(", ");
    return `const { ${importNames} } = require(${JSON.stringify(moduleId(depPath))});`;
  });

  transformed = transformed.replace(IMPORT_SIDE_EFFECT_RE, (_, specifier) => {
    const depPath = resolveImport(filePath, specifier);
    dependencies.push(depPath);
    return `require(${JSON.stringify(moduleId(depPath))});`;
  });

  transformed = transformed.replace(EXPORT_FUNCTION_RE, (_, name) => {
    exports.push(name);
    return `function ${name}(`;
  });
  transformed = transformed.replace(EXPORT_CONST_RE, (_, name) => {
    exports.push(name);
    return `const ${name} =`;
  });
  transformed = transformed.replace(EXPORT_LET_RE, (_, name) => {
    exports.push(name);
    return `let ${name} =`;
  });
  transformed = transformed.replace(EXPORT_VAR_RE, (_, name) => {
    exports.push(name);
    return `var ${name} =`;
  });

  if (/^\s*import\s/m.test(transformed) || /^\s*export\s/m.test(transformed)) {
    throw new Error(`Unsupported import/export syntax in ${filePath}`);
  }

  const uniqueExports = Array.from(new Set(exports));
  if (uniqueExports.length) {
    transformed += `\n\n${uniqueExports.map((name) => `exports.${name} = ${name};`).join("\n")}\n`;
  }

  return { dependencies, transformed };
}

function walk(entryPath) {
  const seen = new Set();
  const order = [];

  function visit(filePath) {
    const resolved = path.resolve(filePath);
    if (seen.has(resolved)) return;
    seen.add(resolved);
    const { dependencies } = parseModule(resolved);
    for (const dep of dependencies) {
      visit(dep);
    }
    order.push(resolved);
  }

  visit(entryPath);
  return order;
}

function build() {
  const files = walk(entryFile);
  const moduleEntries = files
    .map((filePath) => {
      const id = moduleId(filePath);
      const { transformed } = parseModule(filePath);
      return `    ${JSON.stringify(id)}: function(module, exports, require) {\n${transformed
        .split("\n")
        .map((line) => `      ${line}`)
        .join("\n")}\n    }`;
    })
    .join(",\n");

  const bundle = `// Generated by extension/scripts/build_content_bundle.mjs. Do not edit by hand.
(function() {
  const modules = {
${moduleEntries}
  };
  const cache = Object.create(null);
  function require(id) {
    if (cache[id]) {
      return cache[id].exports;
    }
    const factory = modules[id];
    if (!factory) {
      throw new Error("Module not found: " + id);
    }
    const module = { exports: {} };
    cache[id] = module;
    factory(module, module.exports, require);
    return module.exports;
  }
  require("content/index.js");
})();
`;

  fs.writeFileSync(outputFile, bundle, "utf8");
  console.log(`✅ Generated ${toPosix(path.relative(extensionRoot, outputFile))}`);
}

build();
